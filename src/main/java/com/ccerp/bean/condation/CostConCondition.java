package com.ccerp.bean.condation;

public class CostConCondition {

	private String rows;

	private String pages;
	private String sessionId;
	private String payOrg2Code;
	private String orgId;
	private String empName;
	private String empNumber;
	private String contractCode;
	private String personId;
	private String costConId;
	public String getRows() {
		return rows;
	}
	public void setRows(String rows) {
		this.rows = rows;
	}
	public String getPages() {
		return pages;
	}
	public void setPages(String pages) {
		this.pages = pages;
	}
	public String getSessionId() {
		return sessionId;
	}
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	public String getPayOrg2Code() {
		return payOrg2Code;
	}
	public void setPayOrg2Code(String payOrg2Code) {
		this.payOrg2Code = payOrg2Code;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpNumber() {
		return empNumber;
	}
	public void setEmpNumber(String empNumber) {
		this.empNumber = empNumber;
	}
	public String getContractCode() {
		return contractCode;
	}
	public void setContractCode(String contractCode) {
		this.contractCode = contractCode;
	}
	public String getPersonId() {
		return personId;
	}
	public void setPersonId(String personId) {
		this.personId = personId;
	}
	public String getCostConId() {
		return costConId;
	}
	public void setCostConId(String costConId) {
		this.costConId = costConId;
	}
	@Override
	public String toString() {
		return "CostConCondition [rows=" + rows + ", pages=" + pages
				+ ", sessionId=" + sessionId + ", payOrg2Code=" + payOrg2Code
				+ ", orgId=" + orgId + ", empName=" + empName + ", empNumber="
				+ empNumber + ", contractCode=" + contractCode + ", personId="
				+ personId + ", costConId=" + costConId + "]";
	}

	
	
	
}
