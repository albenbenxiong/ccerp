package com.ccerp.bean.services;

public class CostStatusServicesBean {

	/*RowNum：序号
	CostStatusCode：状态CODE
	CostStatusName：状态名称*/
	
	
	private String RowNum;
	
	private String CostStatusCode;
	
	private String CostStatusName;

	public String getRowNum() {
		return RowNum;
	}

	public void setRowNum(String rowNum) {
		RowNum = rowNum;
	}

	public String getCostStatusCode() {
		return CostStatusCode;
	}

	public void setCostStatusCode(String costStatusCode) {
		CostStatusCode = costStatusCode;
	}

	public String getCostStatusName() {
		return CostStatusName;
	}

	public void setCostStatusName(String costStatusName) {
		CostStatusName = costStatusName;
	}
	
	
	
	
	
	
}
