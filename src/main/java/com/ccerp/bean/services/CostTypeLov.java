package com.ccerp.bean.services;

public class CostTypeLov {

	
	/*RowNum：序号
	CostTypeCode：成本类型代码
	PeCostTypeName：成本类型名称*/
	private String RowNum;
	private String CostTypeCode;
	private String PeCostTypeName;
	public String getRowNum() {
		return RowNum;
	}
	public void setRowNum(String rowNum) {
		RowNum = rowNum;
	}
	public String getCostTypeCode() {
		return CostTypeCode;
	}
	public void setCostTypeCode(String costTypeCode) {
		CostTypeCode = costTypeCode;
	}
	public String getPeCostTypeName() {
		return PeCostTypeName;
	}
	public void setPeCostTypeName(String peCostTypeName) {
		PeCostTypeName = peCostTypeName;
	}
	
	
	
}
